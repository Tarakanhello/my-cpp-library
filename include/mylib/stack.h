#ifndef STACK_H
#define STACK_H


#include <cstddef>
#include <utility>

#include "mylib/memory.h"
#include "mylib/vector.h"

namespace
{

/**
 * @brief Начальная ёмкость стека по умолчанию.
 */
constexpr const size_t initialCapacity{ 8 };

} // end namespace



namespace mylib
{

/**
 * @brief Класс Stack — адаптер контейнера, реализующий структуру данных LIFO (стек).
 *
 * Стек построен поверх шаблонного класса Vector, что обеспечивает динамическое
 * расширение и эффективный доступ к элементам. Все операции выполняются за
 * амортизированное O(1) (кроме shrink_to_fit и сравнения).
 *
 * @tparam T          Тип хранимых элементов.
 * @tparam ALLOCATOR  Аллокатор, используемый для управления памятью (по умолчанию
 *                    MySimpleAllocator<T>).
 *
 * Особенности:
 * - Поддерживает копирование и перемещение.
 * - Метод push() принимает произвольное число аргументов и конструирует элемент
 *   непосредственно в стеке (perfect forwarding).
 * - Предоставляет трёхстороннее сравнение (operator<=>) на основе сравнения
 *   внутреннего вектора.
 * - Все методы, не изменяющие состояние, помечены noexcept, где это возможно.
 */
template<typename T, typename ALLOCATOR = mylib::MySimpleAllocator<T>>
class Stack final
{
private:
    Vector<T, ALLOCATOR> m_vector{}; //!< Внутренний вектор, хранящий элементы стека.

public:
    // ========================================================================
    // Конструкторы
    // ========================================================================
    /**
     * @brief Конструктор по умолчанию.
     * @param alloc Аллокатор, передаваемый внутреннему вектору.
     * @post Стек пуст, начальная ёмкость равна initialCapacity (8).
     */
    explicit Stack(ALLOCATOR alloc = ALLOCATOR())
        : m_vector(initialCapacity, alloc)
    {}

    /**
     * @brief Конструктор с заданной начальной ёмкостью.
     * @param capacity Желаемая начальная ёмкость стека.
     * @param alloc    Аллокатор для внутреннего вектора.
     * @post Ёмкость стека не меньше указанной (зависит от реализации Vector).
     */
    explicit Stack(size_t capacity, ALLOCATOR alloc = ALLOCATOR())
        : m_vector(capacity, alloc)
    {}

    /**
     * @brief Конструктор копирования.
     * @param other Копируемый стек.
     * @post Стек содержит копию всех элементов other.
     */
    Stack(const Stack& other)
        : m_vector{ other.m_vector }
    {}

    /**
     * @brief Конструктор перемещения.
     * @param other Перемещаемый стек.
     * @post other остаётся в валидном, но неопределённом состоянии.
     */
    Stack(Stack&& other) noexcept
        : m_vector{ std::move(other.m_vector) }
    {}

    /**
     * @brief Деструктор.
     * @note Автоматически уничтожает все элементы и освобождает память.
     */
    ~Stack() noexcept = default;


    // ========================================================================
    // Операторы присваивания
    // ========================================================================

    /**
     * @brief Копирующий оператор присваивания.
     * @param other Стек для копирования.
     * @return Ссылка на текущий стек.
     */
    Stack& operator=(const Stack& other)
    {
        if(this != &other)
        {
            m_vector = other.m_vector;
        }

        return *this;
    }

    /**
     * @brief Перемещающий оператор присваивания.
     * @param other Перемещаемый стек.
     * @return Ссылка на текущий стек.
     * @post other остаётся в валидном, но неопределённом состоянии.
     */
    Stack& operator=(Stack&& other) noexcept
    {
        if(this != &other)
        {
            m_vector = std::move(other.m_vector);
        }

        return *this;
    }

    // ========================================================================
    // Основные операции стека
    // ========================================================================

    /**
     * @brief Добавляет новый элемент на вершину стека.
     * @tparam ARGS Типы аргументов для конструирования элемента.
     * @param args Аргументы, передаваемые конструктору T.
     * @throws Может бросить исключение при выделении памяти или при
     *         конструировании элемента. В этом случае стек остаётся неизменным
     *         (гарантия сильной безопасности, обеспечиваемая Vector).
     * @post Размер стека увеличивается на 1, новый элемент становится вершиной.
     */
    template<typename... ARGS>
    void push(ARGS&&... args)
    {
        m_vector.push_back(std::forward<ARGS>(args)...);
    }

    /**
     * @brief Удаляет верхний элемент стека.
     * @note Вызывает деструктор удаляемого элемента.
     * @pre Стек не должен быть пустым (поведение не определено, если empty() == true).
     * @post Размер стека уменьшается на 1.
     */
    void pop() noexcept
    {
        m_vector.resize(m_vector.size() - 1);
    }

    /**
     * @brief Возвращает ссылку на верхний элемент стека (неконстантная версия).
     * @return Ссылка на элемент на вершине стека.
     * @pre Стек не должен быть пустым.
     */
    T& top() noexcept
    {
        return m_vector.back();
    }

    /**
     * @brief Возвращает константную ссылку на верхний элемент стека.
     * @return Константная ссылка на элемент на вершине стека.
     * @pre Стек не должен быть пустым.
     */
    const T& top() const noexcept
    {
        return m_vector.back();
    }

    // ========================================================================
    // Состояние стека
    // ========================================================================

    /**
     * @brief Проверяет, пуст ли стек.
     * @return true, если стек не содержит элементов, иначе false.
     */
    constexpr bool empty() const noexcept
    {
        return m_vector.empty();
    }

    /**
     * @brief Преобразование к bool, позволяющее использовать стек в условных
     *        выражениях.
     * @return true, если стек не пуст, иначе false.
     */
    explicit constexpr operator bool() const noexcept
    {
        return !empty();
    }

    /**
     * @brief Возвращает количество элементов в стеке.
     * @return Текущий размер стека.
     */
    size_t size() const noexcept
    {
        return m_vector.size();
    }

    /**
     * @brief Возвращает текущую ёмкость стека (количество выделенных элементов).
     * @return Ёмкость внутреннего вектора.
     */
    size_t capacity() const noexcept
    {
        return m_vector.capacity();
    }

    // ========================================================================
    // Управление памятью
    // ========================================================================

    /**
     * @brief Удаляет все элементы из стека.
     * @post Стек становится пустым, размер равен 0.
     * @note Ёмкость не изменяется (память не освобождается).
     */
    void clear() noexcept
    {
        m_vector.clear();
    }

    /**
     * @brief Запрашивает уменьшение ёмкости до размера стека.
     * @throws Может бросить исключение при перераспределении памяти.
     * @post Ёмкость становится не меньше текущего размера (обычно равна размеру).
     */
    void shrink_to_fit()
    {
        m_vector.shrink_to_fit();
    }

    // ========================================================================
    // Сравнение
    // ========================================================================
    /**
     * @brief Трёхстороннее сравнение двух стеков (лексикографическое).
     * @param other Стек для сравнения.
     * @return Результат сравнения (std::strong_ordering или эквивалент).
     * @note Сравнение выполняется поэлементно с помощью operator<=> для T.
     */
    auto operator<=>(const Stack& other) const
    {
        return m_vector <=> other.m_vector;
    }
};

} // end namespace


#endif // STACK_H
